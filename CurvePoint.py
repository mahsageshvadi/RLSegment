
class CurvePoint:
    def __init__(self, pos, radius):
        self.pos = pos
        self.radius = radius

    def get_pos(self):
        return self.pos

    def get_radius(self):
        return self.radius
    
    def set_pos(self, pos):
        self.pos = pos
    
    def set_radius(self, radius):
        self.radius = radius
    
    
